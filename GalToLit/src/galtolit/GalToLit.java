public class GalToLit {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        double gallon;
        double liters;
        
        gallon = 10;
        liters = gallon * 3.7854;
        System.out.println(gallon + " gallons is " + liters + " liters");
    }
}
